    public ArrayList<Concepto> traer_data(){
        ArrayList<Concepto> lista=new ArrayList();
        try {
            lista = cliente_concepto.buscar_concepto();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error Conectando al servidor \n",
                    "Informacion", JOptionPane.ERROR_MESSAGE);
        }
        return lista;
    }
    
    public void llenar_tabla(){
        ArrayList<Concepto> lista = traer_data();
        DefaultTableModel modelo = new DefaultTableModel();
        String[] col = {"ID","DESCRIPCION"};
        // ciclo for para agregar cada una de las columnas
        for (int i = 0; i < col.length; i++) {
            modelo.addColumn(col[i]);
        }
        
        int k;
        for(Concepto c:lista){
            k=0;
            Object[] fila = new Object[2];//El tamaÃ±o del vector sera la cantidad de columnas de la tabla
            fila[k++] = (Object)c.getId_concepto();
            fila[k++] = (Object)c.getDescripcion();

            modelo.addRow(fila);
        }
        tbConcepto.setModel(modelo);
    }
    

